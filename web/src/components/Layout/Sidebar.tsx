export function Sidebar() {
  const navigate = useNavigate();
  const user = useUser();
  const authEnabled = useAuthEnabled();
  const logout = useAuthStore((state) => state.logout);

  const handleLogout = async () => {
    try {
      await api.logout();
    } catch {
      // Ignore errors, logout locally anyway
    }
    logout();
    navigate('/login');
  };

  return (
    <aside className="w-56 bg-gray-800 border-r border-gray-700 flex flex-col">
      <nav className="flex-1 py-4">
        <ul className="space-y-1">
          {navItems.map((item) => (
            <li key={item.path}>
              <NavLink
                to={item.path}
                className={({ isActive }) =>
                  `flex items-center px-4 py-2.5 text-sm font-medium transition-colors ${
                    isActive
                      ? 'bg-blue-600 text-white'
                      : 'text-gray-300 hover:bg-gray-700 hover:text-white'
                  }`
                }
              >
                <span className="mr-3">{icons[item.icon]}</span>
                {item.label}
              </NavLink>
            </li>
          ))}
        </ul>
      </nav>

      <div className="border-t border-gray-700">
        {/* User info and logout (only when auth is enabled) */}
        {authEnabled && user && (
          <div className="p-4 space-y-3">
            <div className="flex items-center text-gray-300">
              <span className="mr-2">{icons.user}</span>
              <span className="text-sm truncate">{user.username}</span>
            </div>
            <button
              onClick={handleLogout}
              className="w-full flex items-center px-3 py-2 text-sm font-medium text-gray-300 hover:bg-gray-700 hover:text-white rounded transition-colors"
            >
              <span className="mr-2">{icons.logout}</span>
              Sign out
            </button>
          </div>
        )}

        <div className="p-4 border-t border-gray-700">
          <p className="text-xs text-gray-500 text-center">
            Open-UAV Telemetry Bridge
          </p>
        </div>
      </div>
    </aside>
  );
}

export default Sidebar;
